CREATE EXTENSION IF NOT EXISTS postgis;

CREATE SCHEMA IF NOT EXISTS staging;
CREATE SCHEMA IF NOT EXISTS intermediate;
CREATE SCHEMA IF NOT EXISTS production;


-- JKK importide hoimise tabel
CREATE TABLE IF NOT EXISTS staging.raw_snapshot (
    run_id uuid PRIMARY KEY,
    fetched_at timestamptz NOT NULL,
    source_name text NOT NULL,
    row_count int,
    raw_data jsonb NOT NULL,
    status text NOT NULL,
    message text
);

-- JKK registri puhastatud vahetabel

CREATE TABLE IF NOT EXISTS intermediate.jkk_curr_clean (
    oid bigint GENERATED BY DEFAULT AS IDENTITY,
    
    -- Otse API-st pärinevad väljad
    objekti_nimetus text,
    jkk_kood text,
    jkk_olukord text,
    kaitaja_nimi text,
    kaitaja_kood text,
    aadress text,
    teised_aadressid text,
    x_koordinaat integer,
    y_koordinaat integer,
    tegevus text,
    tegevus_selg text,
    tegevuse_tapsustus text,
    tegevuse_algus date,
    tegevuse_lopp date,
    muudetud text,
    komplekstegevus text,
    komplekstegevus_selg text, 
    jaatmete_kaitlemine text,
    
    -- Järeltöödeldud väljad
    jkk_kood_ext text, -- jkk_kood + kat_id > unikaalne obj sihtsüsteemi jaoks
    nimi text,
    lyhinimi text,
    brand text,
    kat_id integer,
    liigisona text,
    lipikud text,

    -- Geomeetria veerg, täitub x-y põhjal automaatselt
    geom geometry(Point, 3301) GENERATED ALWAYS AS (
        CASE
            WHEN x_koordinaat IS NULL OR y_koordinaat IS NULL THEN NULL
            ELSE ST_SetSRID(
                ST_MakePoint(y_koordinaat, x_koordinaat),
                3301
            )
        END
    ) STORED,
    CONSTRAINT pk_jkk_curr_clean PRIMARY KEY (oid)
);

CREATE INDEX IF NOT EXISTS ix_jkk_curr_clean_geom
ON intermediate.jkk_curr_clean
USING gist (geom);

