-- Muutuste kumulatiivne tabel

CREATE TABLE IF NOT EXISTS production.jkk_changes (
    oid bigint GENERATED BY DEFAULT AS IDENTITY,
    objekti_nimetus text,
    jkk_kood_ext text,
    -- Nime muutus
    nimi_change_status integer DEFAULT 0,
    nimi_old text,
    nimi_new text,
    -- Haldaja / brandi muutus
    brand_change_status integer DEFAULT 0,
    brand_old text,
    brand_new text,
    -- Liigisõna muutus
    liigisona_change_status integer DEFAULT 0,
    liigisona_old text,
    liigisona_new text,
    -- Lipikute muutus
    lipikud_change_status integer DEFAULT 0,
    lipikud_old text,
    lipikud_new text,
    -- Geomeetria muutus
    geom_change_status integer DEFAULT 0,
    geom_change geometry(LineString, 3301),
    -- Käsitlemise väljad
    kommentaar text,
    change_date date,
    resolved_date date,
    geom geometry(Point, 3301),
    CONSTRAINT pk_jkk_changes PRIMARY KEY (oid)
);

CREATE INDEX IF NOT EXISTS ix_jkk_changes_geom
ON production.jkk_changes
USING gist (geom);

CREATE INDEX IF NOT EXISTS ix_jkk_changes_geom_change
ON production.jkk_changes
USING gist (geom_change);